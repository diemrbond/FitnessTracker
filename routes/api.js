const router = require("express").Router();
const Workout = require("../models/workouts.js");

router.post("/workouts", ({ body }, res) => {
    Workout.create(body)
        .then(dbWorkout => {
            res.json(dbWorkout);
        })
        .catch(err => {
            res.status(400).json(err);
        });
});

router.put("/workouts/:id", ({ body, params }, res) => {
    Workout.findByIdAndUpdate(
        params.id,

        { $push: { exercises: body } },
    )
        .then((dbWorkout) => {
            res.json(dbWorkout);
        })
        .catch((error) => {
            res.json(error);
        });
});

